'''
 Escribe un programa en Python que convierta
 las siguientes temperaturas de grados Celsius a 
 grados Fahrenheit:
  30 ºC
 -273.99 ºC
  100 ºC
'''
a = 30
b = -273
c = 100
F_a = (a * 1.8)+32
F_b = (b * 1.8)+32
F_c = (c * 1.8)+32

print("para 30ºC --> Fahrenheit: ",F_a)
print("para -273ºC --> Fahrenheit: ",F_b)
print("para 100ºC --> Fahrenheit: ",F_c)